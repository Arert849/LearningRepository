a, b = 10, 3

print(a == b)   # Returns False, compares whether a is equal to b
print(a != b)   # Return True, compares whether a is NOT equal to b
print(a > b)    # Returns True, compares whether a is greater than b
print(a <= b)   # Returns False, compares whether a is less than or equal to b
print(a >= b)   # Returns True, compares whether a is greater than or equal to b
